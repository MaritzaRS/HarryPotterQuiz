import java.awt.Color;

public class Ravenclaw extends House{
	
	private String co;
	private Color r;
	
	public Ravenclaw() {
		super();
		co = "Wises";
		r = Color.BLUE;
	}

	public String getCo() {
		return co;
	}

	public void setCo(String co) {
		this.co = co;
	}

	public Color getR() {
		return r;
	}

	public void setR(Color r) {
		this.r = r;
	}
	
	
	
}
